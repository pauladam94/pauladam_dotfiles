vim.api.nvim_create_user_command("Format", function(_)
  if vim.bo.filetype == "lua" then
    vim.cmd ":!stylua %"
  elseif vim.bo.filetype == "nix" then
    vim.cmd ":!nixpkgs-fmt %"
  else
    vim.lsp.buf.format()
  end
end, { desc = "Format current buffer with LSP" })

