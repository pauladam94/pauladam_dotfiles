    vim.cmd(":!typstfmt %")
  elseif vim.bo.filetype == "nix" then
    vim.cmd(":!nixpkgs-fmt %")
  else
    vim.lsp.buf.format()
  end
end, { desc = 'Format current buffer with LSP' })
